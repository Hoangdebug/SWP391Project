package model.entity;

/**
 *
 * @author ACER
 */
public class Orders {

    private int id;
    private int user_id;
    private String method;
    private String status;

    public Orders(int id, int user_id, String method, String status) {
        this.id = id;
        this.user_id = user_id;
        this.method = method;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
